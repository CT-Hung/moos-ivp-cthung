//---------------------------------------------------------
// Procedure: Iterate()
//            happens AppTick times per second

bool HazardMgr::Iterate()
{
  AppCastingMOOSApp::Iterate();
	double incurrent_time;
	double wait_finish;
	double frequency;
	incurrent_time = MOOSTime();
	wait_finish = incurrent_time-m_wait_time;
	frequency = incurrent_time-m_post_time;

	if(m_start == "true" && wait_finish >= 150 && m_got_finish != "true"){
		if(frequency >= 65){
			postFinishMail();
			m_wait_time = incurrent_time;
			m_post_time = incurrent_time;
		}
	}else if(m_start_post == "true" && frequency >= 65){
		postGotFinishMail();
		m_post_time = incurrent_time;
		m_start_post = "false";
		
	}else if(m_got_finish == "true" && frequency >= 65){
		postCollMail();
		m_post_time = incurrent_time;
		m_got_finish = "false";
	}


  if(!m_sensor_config_requested)
    postSensorConfigRequest();

  if(m_sensor_config_set)
    postSensorInfoRequest();

  AppCastingMOOSApp::PostReport();
  return(true);
}

//---------------------------------------------------------
// Procedure: OnStartUp()
//            happens before connection is open

bool HazardMgr::OnStartUp()
{
  AppCastingMOOSApp::OnStartUp();

  STRING_LIST sParams;
  m_MissionReader.EnableVerbatimQuoting(true);
  if(!m_MissionReader.GetConfiguration(GetAppName(), sParams))
    reportConfigWarning("No config block found for " + GetAppName());

  STRING_LIST::iterator p;
  for(p=sParams.begin(); p!=sParams.end(); p++) {
    string orig  = *p;
    string line  = *p;
    string param = tolower(biteStringX(line, '='));
    string value = line;

    bool handled = false;
    if((param == "swath_width") && isNumber(value)) {
      m_swath_width_desired = atof(value.c_str());
      handled = true;
    }
    else if(((param == "sensor_pd") || (param == "pd")) && isNumber(value)) {
      m_pd_desired = atof(value.c_str());
      handled = true;
    }
    else if(param == "report_name") {
      value = stripQuotes(value);
      m_report_name = value;
      handled = true;
    }
    else if(param == "region") {
      XYPolygon poly = string2Poly(value);
      if(poly.is_convex())
	m_search_region = poly;
      handled = true;
    }else if(param == "cname"){
			m_cname = value;
			handled = true;
		}

    if(!handled)
      reportUnhandledConfigWarning(orig);
  }
  
  m_hazard_set.setSource(m_host_community);
  m_hazard_set.setName(m_report_name);
  m_hazard_set.setRegion(m_search_region);
  
  registerVariables();	
  return(true);
}

